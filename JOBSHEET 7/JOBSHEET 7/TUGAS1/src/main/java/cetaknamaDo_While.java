public class cetaknamaDo_While {
        public static void main(String[] args) {
        String nama = "JIHAN SALSABILA";
        int jumlahCetak = 100;
        int i = 0;
        
        do {
            System.out.println(nama);
            i++;
        } while (i < jumlahCetak);
    }
}

